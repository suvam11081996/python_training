'''

Immutable Types
  - Numbers
    -- We have option to store Number like int, float etc
  - Strings
    -- We have option to store collection of Characters like name, emil id etc
    -- For each character, automatically index will be assigned
  - tuple
    -- We have option to store collection of values like list of name, list of email ids etc
    -- We can store duplicate values
    -- For each values, automatically index number will be assigned
  frozenset
    -- We have option to store collection of values like list of name, list of email ids etc
    -- We can store unique values
    -- No index number to each value

Mutable Types

  - list
    -- We have option to store collection of values like list of name, list of email ids etc
    -- We can store duplicate values
    -- For each values, automatically index number will be assigned
  - set
    -- We have option to store collection of values like list of name, list of email ids etc
    -- We can store unique values
  - Dictionary
    -- We have option to store collection of values like list of name, list of email ids etc
    -- We can store duplicate values
    -- We can provide our own index to each value

'''


